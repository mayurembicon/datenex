    function Row($data, $align, $fill = null, $font_weight = null, $is_border = false, $height = 5)
    {
        $this->SetFont('courier', 'B', '');
        $this->SetFont('courier', '', '');

        //Calculate the height of the row
        $nb = 0;
        for ($i = 0; $i < count($data); $i++)
            $nb = max($nb, $this->NbLines($this->widths[$i], $data[$i]));
        $h = $height * $nb;

        //Issue a page break first if needed
        $this->CheckPageBreak($h);

        //Draw the cells of the row
        for ($i = 0; $i < count($data); $i++) {
            if (!empty($font_weight)) {
                if ($font_weight[$i] == 'B') {
                    $this-> SetFont('courier', 'B', '8');
                } else {
                    $this->SetFont('courier', '', 8);

                }
            }

            $w = $this->widths[$i];//
            $a = isset($this->aligns[$i]) ? $this->aligns[$i] : $align;
            //Save the current position
            $x = $this->GetX();
            $y = $this->GetY();
            //Draw the border
            if ($is_border == true) {
                $this->Rect($x, $y, $w, $h, (($fill == true) ? 'DF' : ''));
            }
            //Print the text
            $this->MultiCell($w, $height, $data[$i], 0, $align[$i]);
            //Put the position to the right of the cell
            $this->SetXY($x + $w, $y);
        }
        //Go to the next line
        $this->Ln($h);
        return $nb;
    }

